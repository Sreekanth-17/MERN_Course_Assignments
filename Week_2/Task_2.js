// Given values
let x = 10;
let y = 5;

// Perform the operations
let addition = x + y;         // Addition
let multiplication = x * y;   // Multiplication
let modulus = x % y;          // Modulus

// Output the results
console.log("Addition: " + addition);           // 10 + 5 = 15
console.log("Multiplication: " + multiplication); // 10 * 5 = 50
console.log("Modulus: " + modulus);             // 10 % 5 = 0
